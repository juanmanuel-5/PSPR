    /**
     * Muestra en el log el mensaje recibido.
     * @param texto Mensaje a mostrar.
     */
    public void escribirLog(String texto)
    {
        if (txtLog.getText().isEmpty())
            txtLog.setText(texto);
        else
            txtLog.setText(txtLog.getText() + "\n" + texto);
    }                                             
    
